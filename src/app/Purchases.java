    private void updatepurchasesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updatepurchasesMouseClicked
        // TODO add your handling code here:
        df = (DefaultTableModel)salestable.getModel();
        int selected = salestable.getSelectedRow();
        int quan = (int)qty.getValue();
        df.setValueAt(quan, selected, 3);
        double cost = Double.parseDouble(price.getText());
        int qty1 = (int)qty.getValue();
        double tot = cost * qty1;
        df.setValueAt(tot, selected, 5);
        double sum = 0.0;
        for(int i=0 ; i<salestable.getRowCount(); i++)
        {
            sum = sum + Double.parseDouble(salestable.getValueAt(i, 5).toString());
        }
        totalsales.setText(String.valueOf(sum));
        pid.setText("");
        item.setText("");
        constype.setText("");
        qty.setValue(0);
        price.setText("");
    }//GEN-LAST:event_updatepurchasesMouseClicked

