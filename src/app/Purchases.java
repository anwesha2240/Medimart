    private void confirmpurchasesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmpurchasesActionPerformed

        try {
            // TODO add your handling code here:
            PreparedStatement pst1;
            pst1 = con.prepareStatement("update `pharmacy_management_system`.`items` set `Quantity` = Quantity - ?, `Price` = Price - ? where `Pid` = ?");
            for(int i=0; i<salestable.getRowCount();i++)
            {
                String productid = salestable.getValueAt(i, 0).toString();
                String qty1 = salestable.getValueAt(i, 3).toString();
                String total = salestable.getValueAt(i, 5).toString();
                pst1.setString(1,qty1);
                pst1.setString(2, total);
                pst1.setString(3,productid);
                pst1.executeUpdate();
            }

            DateTimeFormatter dt = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            LocalDateTime now = LocalDateTime.now();
            String date = dt.format(now);
            int lastid=0;

            String subtotal1 = totalsales.getText();
            PreparedStatement pst2 = con.prepareStatement("insert into `pharmacy_management_system`.`sales` (`date`,`subtotal`) values (?,?); ", Statement.RETURN_GENERATED_KEYS);
            pst2.setString(1, date);
            pst2.setString(2,subtotal1);

            pst2.executeUpdate();
            ResultSet rs1=pst2.getGeneratedKeys();
            if(rs1.next())
            {
                lastid=rs1.getInt(1);
            }
            PreparedStatement pst3 = con.prepareStatement("insert into `pharmacy_management_system`.`salesitem` (`salesid`,`item`,`consumetype`,`quantity`,`price`,`total`) values (?,?,?,?,?,?); ");
            String item1;
            String consumetype;
            String quantity;
            String price1;
            String total;
            for(int i=0; i<salestable.getRowCount();i++)
            {
                item1=salestable.getValueAt(i, 1).toString();
                consumetype=salestable.getValueAt(i, 2).toString();
                quantity=salestable.getValueAt(i, 3).toString();
                price1=salestable.getValueAt(i, 4).toString();
                total=salestable.getValueAt(i, 5).toString();

                pst3.setInt(1,lastid );
                pst3.setString(2,item1 );
                pst3.setString(3,consumetype );
                pst3.setString(4,quantity);
                pst3.setString(5,price1 );
                pst3.setString(6,total );
                pst3.executeUpdate();
            }
        }
        catch (SQLException ex) {
            Logger.getLogger(Sales.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(this,"Sales recorded");

    }//GEN-LAST:event_confirmpurchasesActionPerformed

